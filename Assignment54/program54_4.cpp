template<class T>
void Reverse(T*Arr , int iSize)
{
    int iCnt = 0;
    T temp;
    for(iCnt = 0; iCnt < iSize ; iCnt++)
    {
        if(iCnt <= iSize)
        {
           temp = Arr[iCnt];
           Arr[iCnt] = Arr[iSize-1];
           Arr[iSize-1] = temp;
        }
    }

    iCnt = 0;

    cout<<"Reversed Array";
    for(iCnt = 0 ; iCnt < iSize ; iCnt++)
    {
        cout<<Arr[iCnt]<<" ";
    }
    cout<<"\n";
}

