///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0 ;
    int iRet = 0;
    int iCnt = 0;

    cout<<"Enter the lenght of the array:-\n";
    cin>>iLength;

    int*Brr = NULL ;
    Brr = new int[iLength];

    cout<<"Enter the Elements:-\n";

    for(iCnt = 0 ; iCnt < iLength ; iCnt++)
    {
        cin>>Brr[iCnt];
    }

    iRet = SumEven(Brr, iLength );

    cout<<"Summation of even number :-"<<iRet<<"\n";
    
    delete Brr;

    return 0 ;
}

