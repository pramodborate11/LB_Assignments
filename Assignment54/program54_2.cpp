template <class T>
T SumEven(T*Arr, int iSize )
{
    int iCnt = 0;
    T SumEven = 0;

    for(iCnt = 0 ; iCnt< iSize ; iCnt++)
    {
        if(Arr[iCnt]%2 == 0)
        {
            SumEven = SumEven+ Arr[iCnt];
        }
    }

    return SumEven;
}

