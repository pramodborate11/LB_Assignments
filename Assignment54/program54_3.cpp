template <class T>
T SumOdd(T*Arr, int iSize )
{
    int iCnt = 0;
    T SumOdd = 0;

    for(iCnt = 0 ; iCnt< iSize ; iCnt++)
    {
        if(Arr[iCnt]%2 == 0)
        {
            SumOdd = SumOdd+ Arr[iCnt];
        }
    }

    return SumOdd;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0 ;
    int iRet = 0;
    int iCnt = 0;

    cout<<"Enter the lenght of the array:-\n";
    cin>>iLength;

    int*Brr = NULL ;
    Brr = new int[iLength];

    cout<<"Enter the Elements:-\n";

    for(iCnt = 0 ; iCnt < iLength ; iCnt++)
    {
        cin>>Brr[iCnt];
    }

    iRet = SumOdd(Brr, iLength );

    cout<<"Summation of odd number :-"<<iRet<<"\n";
    
    delete Brr;

    return 0 ;
}

