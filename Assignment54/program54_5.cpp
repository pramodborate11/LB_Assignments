///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name  : Min
//  Description    : Minimum from  N Numbers
//  Input          : Array , Size of Array
//  Output         : Minimum Element
//  Author         : Pramod Khandu Borate
//  Date           : 12/01/2026
//
