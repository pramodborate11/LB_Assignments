template <class T>
T Min(T*Arr , int iSize)
{
    int iCnt = 0;
    T Min ;
    Min = Arr[0];
    for(iCnt = 0; iCnt < iSize  ;iCnt++)
    {
        if(Arr[iCnt] < Min)
        {
            Min = Arr[iCnt];
        }
    }
    return Min ;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0 ;
    int iCnt = 0;
    int iRet = 0 ;

    cout<<"Enter the lenght of the array:-\n";
    cin>>iLength;

    int*Brr = NULL ;
    Brr = new int[iLength];

    cout<<"Enter the Elements:-\n";

    for(iCnt = 0 ; iCnt < iLength ; iCnt++)
    {
        cin>>Brr[iCnt];
    }

    iRet = Min(Brr , iLength);
    cout<<"Minimum elements is :"<<iRet<<"\n";

    delete Brr;

    return 0 ;
}

