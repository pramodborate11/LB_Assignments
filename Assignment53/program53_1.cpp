template <class T>
void CopyArray(T*Src , T*Dest , int iSize)
{
    int iCnt = 0;
    for(iCnt = 0; iCnt < iSize  ;iCnt++)
    {
        Dest[iCnt] = Src[iCnt];
    }
    cout<<"Source Array:- \n";
    for(iCnt = 0; iCnt < iSize  ;iCnt++)
    {
        cout<<Src[iCnt]<<" ";
    }
    cout<<"\n";
    cout<<"Destination Array:- \n";
    for(iCnt = 0; iCnt < iSize  ;iCnt++)
    {
        cout<<Dest[iCnt]<<" ";
    }
    cout<<"\n";
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0 ;
    int iCnt = 0;

    cout<<"Enter the lenght of the array:-\n";
    cin>>iLength;

    int*Brr = NULL ;
    Brr = new int[iLength];

    int*Arr = NULL ;
    Arr = new int[iLength];

    cout<<"Enter the Elements:-\n";

    for(iCnt = 0 ; iCnt < iLength ; iCnt++)
    {
        cin>>Brr[iCnt];
    }

    CopyArray(Brr , Arr , iLength);

    delete Brr;
    delete Arr;

    return 0 ;
}

