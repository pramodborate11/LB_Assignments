///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name  : SecondMax
//  Description    : Return Second Maximum number
//  Input          : Array , Size of Array
//  Output         : Second Maximum number
//  Author         : Pramod Khandu Borate
