#include<stdio.h>
#include<stdlib.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description  : implementation of Singly Linear Linked List
//  Function     : InsertFirst
//  Description  : Insert node at beginning
//  Author       : Borate Pramod Khandu
//  Date         : 03/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////

void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayOdd
//  Discription   : Display Odd Elements
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void DisplayOdd(PNODE first)
{

    while ((first != NULL))
    {
        if(( first->data % 2)  != 0)
        {
            printf("| %d |->", first->data);
        }
        first = first->next ;
    }
    printf("NULL\n");
    
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    PNODE head = NULL ;

    InsertFirst(&head , 51);
    InsertFirst(&head , 22);
    InsertFirst(&head , 110);
    InsertFirst(&head ,101);
    InsertFirst(&head ,222);
    InsertFirst(&head ,121);
    InsertFirst(&head ,122);

    DisplayOdd(head );

    return 0 ;
}

