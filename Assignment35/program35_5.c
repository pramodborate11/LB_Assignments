//////////////////////////////////////////////////////////////////////////////
//
//  Function Name:  Reverse
//  Description:    To Display a string in reverse
//  Input:          String
//  Output:
//  Author:         Omkar Santosh Durge
//  Date:           28/11/2025
//
///////////////////////////////////////////////////////////////////////////////
void Reverse(char *str)
{

    char *temp = str;
    while (*str != '\0')
    {
        str++;
    }
    str--;
    while (str >= temp)
    {
        printf("%c", *str);
        str--;
    }
}
///////////////////////////////////////////////////////////////////////////////
//
//          Entry point function of the application
//
///////////////////////////////////////////////////////////////////////////////
int main()
{
    char Arr[50] = {'\0'};

    printf("Enter the string:\n");
    scanf("%[^\n]s", Arr);

    Reverse(Arr);

    return 0;
};