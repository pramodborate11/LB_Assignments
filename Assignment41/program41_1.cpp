#include<iostream>                            
using namespace std ;


void Display(int iNo)
{
    static int i = iNo ;
    if(i >= 1)
    {
        printf("%d\t*\t", i);
        i--;
        Display(iNo);
    }
    

}

int main()
{   
    int iValue = 0;

    cout<<"Enter the number:"<<"\n";
    cin>>iValue;

    Display(iValue);   
    return 0;
}

