#include<stdio.h>
#include<stdlib.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description  : implementation of Singly Linear Linked List
//  Function     : InsertFirst
//  Description  : Insert node at beginning
//  Author       : Borate Pramod Khandu
//  Date         : 02/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CountOdd
//  Discription   : Count the number of Odd elements
//
//////////////////////////////////////////////////////////////////////////////////////////////////


int CountOdd(PNODE first )
{
    int iCount = 0 ;

    while ((first != NULL))
    {
        if(( first->data % 2)  != 0)
        {
            iCount++;
        }
        first = first->next ;
    }
    
    return iCount ;
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    PNODE head = NULL ;
    int iRet = 0;

    InsertFirst(&head , 51);
    InsertFirst(&head , 22);
    InsertFirst(&head , 110);
    InsertFirst(&head ,100);
    InsertFirst(&head ,111);
    InsertFirst(&head ,122);
    InsertFirst(&head ,152);

    iRet = CountOdd(head);

    printf(" total number of node which contain Odd number :- %d", iRet );

    return 0 ;
}

