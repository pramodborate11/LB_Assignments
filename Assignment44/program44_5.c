//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description : implementation of Singly Linear Linked List
// Function     : InsertFirst
// Description  : Insert node at beginning
//  Author      : Shubham Shankarlal Kumawat
//  Date        : 01/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayEven
//  Discription   : Display Even Elements
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void DisplayEven(PNODE first)
{

    while ((first != NULL))
    {
        if(( first->data % 2)  == 0)
        {
            printf("| %d |->", first->data);
        }
        first = first->next ;
    }
    printf("NULL\n");
    
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    PNODE head = NULL ;

    InsertFirst(&head , 51);
    InsertFirst(&head , 22);
    InsertFirst(&head , 110);
    InsertFirst(&head ,101);
    InsertFirst(&head ,222);
    InsertFirst(&head ,121);
    InsertFirst(&head ,122);

    DisplayEven(head );

    return 0 ;
}

