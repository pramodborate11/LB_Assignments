#include<stdio.h>
#include<stdlib.h>
#include<stdbool.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description  : implementation of Singly Linear Linked List
//  Function     : InsertFirst
//  Description  : Insert node at beginning
//  Author       : Borate Pramod Khandu
//  Date         : 03/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////

void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : IsEmpty
//  Description :   check whether list is empty or not
//  Input :         Address
//
///////////////////////////////////////////////////////////////////////////////

bool IsEmpty(PNODE first )
{
    if(first == NULL)
    {
        return true;
    }
    else
    {
        return false;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    PNODE head = NULL ;
    bool bRet = false;

    InsertFirst(&head , 51);
    InsertFirst(&head , 12);
    InsertFirst(&head , 110);
    InsertFirst(&head ,101);
    InsertFirst(&head ,22);
    InsertFirst(&head ,121);
    InsertFirst(&head ,51);

    bRet = IsEmpty(head);

    if(bRet == true)
    {
        printf("linked list is empty\n");
    }
    else
    {
        printf("linked list is not empty\n");
    }

    return 0 ;
}

