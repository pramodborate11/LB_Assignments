///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name  : Max
//  Description    : Maximum from  N Numbers
//  Input          : Array , Size of Array
//  Output         : Maximum Element
//  Author         : Pramod Khandu Borate
