///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

template <class T>
T Max(T*Arr , int iSize)
{
    int iCnt = 0;
    T Max ;
    Max = Arr[0];
    for(iCnt = 0; iCnt < iSize  ;iCnt++)
    {
        if(Arr[iCnt] > Max)
        {
            Max = Arr[iCnt];
        }
    }
    return Max ;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0 ;
    int iCnt = 0;
    int iRet = 0 ;

    cout<<"Enter the lenght of the array:-\n";
    cin>>iLength;

    int*Brr = NULL ;
    Brr = new int[iLength];

    cout<<"Enter the Elements:-\n";

    for(iCnt = 0 ; iCnt < iLength ; iCnt++)
    {
        cin>>Brr[iCnt];
    }

    iRet = Max(Brr , iLength);
    cout<<"Maximum elements is :"<<iRet<<"\n";

    delete Brr;

    return 0 ;
}

