template <class T>
T AddN(T *Arr, int iSize)
{
    int iCnt = 0;
    T Sum = 0;
    for (iCnt = 0; iCnt < iSize; iCnt++)
    {
        Sum = Sum + Arr[iCnt];
    }
    return Sum;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iLength = 0;
    int iCnt = 0;
    int iRet = 0;

    cout << "Enter the lenght of the array:-\n";
    cin >> iLength;

    int *Brr = NULL;
    Brr = new int[iLength];

    cout << "Enter the Elements:-\n";

    for (iCnt = 0; iCnt < iLength; iCnt++)
    {
        cin >> Brr[iCnt];
    }

    iRet = AddN(Brr, iLength);
    cout << "Sum of elements is :" << iRet << "\n";

    delete Brr;
    return 0;
}

