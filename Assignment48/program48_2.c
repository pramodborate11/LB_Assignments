int main()
{
    PNODE head = NULL ;

    InsertFirst(&head , 51);
    InsertFirst(&head , 22);
    InsertFirst(&head , 111);
    InsertFirst(&head ,100);
    InsertFirst(&head ,111);
    InsertFirst(&head ,121);
    InsertFirst(&head ,152);
    Display(head);
    ReplaceOdd(head);

    return 0 ;
}

