//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description  : implementation of Singly Linear Linked List
//  Function     : InsertFirst
//  Description  : Insert node at beginning
//  Author       : Borate Pramod Khandu
//  Date         : 04/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////

void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   display the Linked list 
//  Input :         Address
//
///////////////////////////////////////////////////////////////////////////////

void Display(PNODE first)
{
    int iCnt = 0;
    printf("Input linked list :");

    while(first != NULL)
    {
        printf(" |%d| ->",first->data);
        first = first->next ;
    }
    printf("NULL\n");
}
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : ReplaceOdd
//  Discription   : replace Odd number with 0
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void ReplaceOdd(PNODE first )
{
    printf("Updated Linked List :- ");
    while ((first != NULL))
    {
        if(( first->data % 2)  != 0)
        {
            first->data = 1;
        }
        printf("| %d |->", first->data);
        first = first->next ;
    }
    printf("NULL\n");

}

//////////////////////////////////////////////////////////////////////////////////////////////////
