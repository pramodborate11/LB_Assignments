#include<stdio.h>
#include<stdlib.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Structure for a node of singly linked list
//
//////////////////////////////////////////////////////////////////////////////////////////////////

struct node
{
    int data ; 
    struct node*next;
};

typedef struct node NODE;
typedef struct node* PNODE ;
typedef struct node** PPNODE;

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Description  : implementation of Singly Linear Linked List
//  Function     : InsertFirst
//  Description  : Insert node at beginning
//  Author       : Borate Pramod Khandu
//  Date         : 04/01/2026
//
//////////////////////////////////////////////////////////////////////////////////////////////////

void InsertFirst(PPNODE first , int iNo)
{
    PNODE newn = NULL;

    newn = (PNODE)malloc(sizeof(NODE));

    newn->data= iNo ;
    newn->next =  NULL;

    if((*first) == NULL)
    {
        (*first) = newn;
    }
    else
    {
        newn->next = (*first);
        (*first) = newn;
    }
}

///////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description :   display the Linked list 
//  Input :         Address
//
///////////////////////////////////////////////////////////////////////////////

void Display(PNODE first)
{
    printf("Input linked list :");

    while(first != NULL)
    {
        printf(" |%d| ->",first->data);
        first = first->next ;
    }
    printf("NULL\n");
}
//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultiplyByTwo
//  Discription   : multiply each element by two
//
//////////////////////////////////////////////////////////////////////////////////////////////////


void MultiplyByTwo(PNODE first )
{
    printf("Updated Linked List :- ");
    while(first != NULL)
    {
        printf(" |%d| ->",(2*(first->data)));
        first = first->next ;
    }
    printf("NULL\n");
}

//////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry Point Function
//
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    PNODE head = NULL ;

    InsertFirst(&head , 51);
    InsertFirst(&head , 22);
    InsertFirst(&head , 111);
    InsertFirst(&head ,100);
    InsertFirst(&head ,111);
    InsertFirst(&head ,121);
    InsertFirst(&head ,152);
    Display(head);
    MultiplyByTwo(head);

    return 0 ;
}

