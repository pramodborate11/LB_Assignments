void DisplayAscii()
{
    char ch = 0;
    for (ch = 0; ch <= 127; ch++)
    {
        printf("Num:%d\tValue:%c\tHexValue:%x\n", ch, ch, ch);
        if (ch==127)
        {
            break;            /* code */
        }
        
    }
}
