template <class T>

T Substraction(T No1 , T No2)
{
    T Ans = 0;
    Ans = No1-No2;
    return Ans ;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0 , iValue2 = 0;

    cout<<"Enter  First  Number:-\n";
    cin>>iValue1;
    cout<<"Enter Second Number:-\n";
    cin>>iValue2;

    int iRet = 0;
    iRet = Substraction(iValue1, iValue2);
    cout<<"Substraction of Number:-"<<iRet<<"\n";
    return 0 ;

}

