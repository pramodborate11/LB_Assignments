template <class T>

T Max(T No1 , T No2 , T No3 , T no4)
{
   if(No1 > No2)
   {
        return No1;
   }
   else if(No2 > No3)
   {
    return No2;
   }
   else if(No3>no4)
   {
    return No3 ;
   }
   else
   {
    return no4;
   }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0 , iValue2 = 0 , iValue3 = 0 , iValue4;

    cout<<"Enter  First  Number:-\n";
    cin>>iValue1;
    cout<<"Enter Second Number:-\n";
    cin>>iValue2;
    cout<<"Enter third Number:-\n";
    cin>>iValue3;
    cout<<"Enter forth Number:-\n";
    cin>>iValue4;
    
    int iRet = 0;
    iRet = Max(iValue1, iValue2 , iValue3 , iValue4);
    cout<<"Maximum number is:-"<<iRet<<"\n";
    return 0 ;

}

