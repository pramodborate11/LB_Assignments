template <class T>

void Swap(T No1 , T No2)
{
    cout<<"Number befor swap 'No1' and 'No2' "<<No1<<" "<< No2<<"\n";
    T temp = 0 ;
    temp = No1;
    No1 = No2 ;
    No2 = temp ;
    cout<<"Number After swap 'No1' and 'No2' "<<No1<<" " << No2<<"\n";
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//     Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0 , iValue2 = 0;

    cout<<"Enter  First  Number:-\n";
    cin>>iValue1;
    cout<<"Enter Second Number:-\n";
    cin>>iValue2;
    Swap(iValue1, iValue2);
    return 0 ;

}

