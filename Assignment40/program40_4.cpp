#include<iostream>                                 
using namespace std ;


void Display(int iNo)
{
    static int iCnt = 0 ;
    static char ch = 'A';
    if(iCnt < iNo)
    {
        printf("%c\t", ch);
        ch++ ;
        iCnt++;
        Display(iNo);
    }
    

}

int main()
{   
    int iValue = 0;

    cout<<"Enter the number:"<<"\n";
    cin>>iValue;

    Display(iValue);   
    return 0;
}

